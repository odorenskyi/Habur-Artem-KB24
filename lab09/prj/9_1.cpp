#include <iostream>
#include <windows.h>
#include <string>
using namespace std;

void task1(){
    SetConsoleOutputCP(65001); 

    int бал;

    while (true) {
        cout << "\nВведіть бал сили вітру за шкалою Бофорта (0–12): ";
        cin >> бал;

        if (бал < 0 || бал > 12) {
            cout << "Некоректне значення бала. Введіть число від 0 до 12." << endl;
            continue;
        }

        string швидкість, характеристика;

        switch (бал) {
            case 0: швидкість = "< 0.3"; характеристика = "Штиль"; break;
            case 1: швидкість = "0.3 – 1.5"; характеристика = "Тихий вітер"; break;
            case 2: швидкість = "1.6 – 3.3"; характеристика = "Легкий вітер"; break;
            case 3: швидкість = "3.4 – 5.4"; характеристика = "Слабкий вітер"; break;
            case 4: швидкість = "5.5 – 7.9"; характеристика = "Помірний вітер"; break;
            case 5: швидкість = "8.0 – 10.7"; характеристика = "Свіжий вітер"; break;
            case 6: швидкість = "10.8 – 13.8"; характеристика = "Сильний вітер"; break;
            case 7: швидкість = "13.9 – 17.1"; характеристика = "Міцний вітер"; break;
            case 8: швидкість = "17.2 – 20.7"; характеристика = "Дуже міцний вітер"; break;
            case 9: швидкість = "20.8 – 24.4"; характеристика = "Шторм"; break;
            case 10: швидкість = "24.5 – 28.4"; характеристика = "Сильний шторм"; break;
            case 11: швидкість = "28.5 – 32.6"; характеристика = "Шквальний шторм"; break;
            case 12: швидкість = "≥ 32.7"; характеристика = "Ураган (буревій)"; break;
        }

        cout << "Швидкість вітру: " << швидкість << " м/с" << endl;
        cout << "Характеристика: " << характеристика << endl;

        char продовжити;
        cout << "\nБажаєте продовжити (y/n)? ";
        cin >> продовжити;

        if (продовжити == 'n' || продовжити == 'N') {
            cout << "Програма завершена." << endl;
            break;
        }
    }
}
